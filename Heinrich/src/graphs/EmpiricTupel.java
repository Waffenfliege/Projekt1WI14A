package graphs;

public class EmpiricTupel
{

	private float summedQuote;
	private float width;
	
	public EmpiricTupel(float summedQuote, float lineWidth)
	{
		this.summedQuote = summedQuote;
		this.width = lineWidth;
	}

	public float getSummedQuote()
	{
		return summedQuote;
	}

	public float getWidth()
	{
		return width;
	}

	
}
