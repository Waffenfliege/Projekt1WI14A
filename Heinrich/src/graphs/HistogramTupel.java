package graphs;

public class HistogramTupel {
	
	float height;
	float width;
	
	public HistogramTupel(float height, float width){
		this.width = width;
		this.height = height;
	}

	public float getHeight() {
		return height;
	}

	public void setHeight(float height) {
		this.height = height;
	}

	public float getWidth() {
		return width;
	}

	public void setWidth(float width) {
		this.width = width;
	}
}
